	                    <div class="row text-justify">
	                    <h4>Paguyuban Alam Pakidulan Sukabumi (PAPSI) adalah sebuah organisasi masyarakat yang bergerak dibidang konservasi. PAPSI sendiri didirikan pada tahun 2013 oleh sekelompok orang yang bertekad melestarikan Alam Pakidulan Sukabumi yang bertempat di Tamanjaya-Ciemas.

Anggota yang telah bergabung pada saat ini baru sedikit yang berasal dari berbagai latar belakang dan profesi sebagai partisipan dan simpatisan Paguyuban dimana orang tersebut peduli akan alam pakidulan Sukabumi.

PAPSI percaya bahwa Potensi Alam yang dimiliki Alam Pakidulan Sukabumi sangat melimpah, kaya akan keindahan dan dijadikan sarana pendidikan yang diperoleh dari proses pembentukan alam yang khas, adalah milik yang paling berharga daerah ini dan karena itu perlu diperhatikan untuk dilestarikan serta diperkokoh kekuatan alam yang kaya akan pengetahuan ini serta dikembangkan untuk kesejahteraan masyarakat.
<div class="row text-center">
I. Profil
</div>
Paguyuban Alam Pakidulan Sukabumi adalah sebuah Organisasi Masyarakat bersifat non-profit yang didirikan sejumlah orang dalam bidang tertentu dalam rangka melestarikan alam pakidulan khususnya dalam pengembangan Kawasan Geopark, yang kemudian mulai membentuk paguyuban ini atas dasar kecintaannya pada Alam Pakidulan Sukabumi.
<div class="row text-center">
II. Maksud
</div>
Paguyuban Alam Pakidulan Sukabumi (PAPSI) didirikan dengan maksud turut berperan serta aktif dalam upaya-upaya melindungi dan melestarikan alam.
<div class="row text-center">
III. Tujuan
</div>
Paguyuban Alam Pakidulan Sukabumi mempunyai tujuan menghimpun potensi alam pakidulan Sukabumi bersama-sama dengan mengembangkan dan menjadikannya wisata alam yang mempunyai nilai pendidikan, serta mempublikasikannya dengan mengupayakan kesejahteraan masyarakat.
<div class="row text-center">
IV. Relawan dan Keanggotaan
</div>
Untuk menjadi relawan/volunteer atau anggota di Paguyuban Alam Pakidulan Sukabumi (PAPSI), anda harus sanggup meluangkan waktu, pemikiran, dan tenaga tanpa dibayar bahkan terkadang harus sanggup menggeluarkan dari biaya kantong sendiri.
Selama menjadi anggota PAPSI anda harus memiliki komitmen untuk memberikan keahlian yang anda miliki, dapat bekerja dalam team. Mempunyai visi yang sama yaitu melestarikan dan mengembangkan Alam Pakidulan untuk kesejahteraan masyarakat.</h4>
